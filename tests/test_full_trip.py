def print_section_header(title):
    """Print a section header"""
    print("\n" + "-"*100)
    print(f"  {title}")
    print("-"*100 + "\n")


def print_subsection(title):
    """Print a subsection separator"""
    print("\n" + "-"*80)
    print(title)
    print("-"*80 + "\n")


