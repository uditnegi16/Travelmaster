def format_hotel_details(hotel):
    """Format a single hotel's comprehensive details"""
    # Handle both dict and EnrichedHotel object
    if hasattr(hotel, '__dict__'):
        # It's an object
        name = getattr(hotel, "name", "N/A")
        city = getattr(hotel, "city", "N/A")
        address = getattr(hotel, "address", "N/A")
        rating = getattr(hotel, "rating", 0)
        price_per_night = getattr(hotel, "price_per_night", 0)
        currency = getattr(hotel, "currency", "INR")
        amenities = getattr(hotel, "amenities", [])
        checkin = getattr(hotel, "check_in", "N/A")
        checkout = getattr(hotel, "check_out", "N/A")
    else:
        # It's a dict
        name = hotel.get("name", "N/A")
        city = hotel.get("city", "N/A")
        address = hotel.get("address", "N/A")
        rating = hotel.get("rating", 0)
        price_per_night = hotel.get("price_per_night", 0)
        currency = hotel.get("currency", "INR")
        amenities = hotel.get("amenities", [])
        checkin = hotel.get("check_in", "N/A")
        checkout = hotel.get("check_out", "N/A")
    
    # Calculate nights and total cost
    try:
        if checkin != "N/A" and checkout != "N/A":
            checkin_date = datetime.fromisoformat(checkin.replace("Z", ""))
            checkout_date = datetime.fromisoformat(checkout.replace("Z", ""))
            nights = (checkout_date - checkin_date).days
            total_cost = price_per_night * nights if isinstance(price_per_night, (int, float)) else 0
        else:
            nights = 0
            total_cost = 0
    except:
        nights = 0
        total_cost = 0
    
    # Format star rating
    stars = "*" * int(rating) if rating else "N/A"
    
    print(f"{name.upper()} * {stars} * {currency}{price_per_night:,.0f}/night" if isinstance(price_per_night, (int, float)) else f"{name.upper()} * {stars} * {currency}{price_per_night}/night")
    print(f"Address: {address}")
    print(f"Amenities: {', '.join(amenities) if amenities else 'N/A'}")
    print(f"Name: {name.upper()}")
    print(f"City: {city.upper()}")
    print(f"Rating: {stars} ({rating}/5)")
    print(f"Price/Night: {currency}{price_per_night:,.0f}" if isinstance(price_per_night, (int, float)) else f"Price/Night: {currency}{price_per_night}")
    print(f"Stay: {checkin[:10] if checkin != 'N/A' else 'N/A'} to {checkout[:10] if checkout != 'N/A' else 'N/A'}")
    if nights > 0:
        print(f"Total Cost: {currency}{total_cost:,.0f} ({nights} nights)")


