def format_weather_rating(score, rating_label):
    """Convert numeric rating to descriptive text"""
    if score >= 9:
        return f"Exceptional conditions - {rating_label}. Perfect for all planned activities with ideal comfort levels."
    elif score >= 8:
        return f"Excellent conditions - {rating_label}. Very favorable weather with great comfort for most activities."
    elif score >= 7:
        return f"Good conditions - {rating_label}. Pleasant weather suitable for outdoor and indoor activities."
    elif score >= 6:
        return f"Fair conditions - {rating_label}. Acceptable weather with some minor limitations for certain activities."
    elif score >= 5:
        return f"Moderate conditions - {rating_label}. Average weather, plan activities accordingly."
    else:
        return f"Challenging conditions - {rating_label}. Weather may impact some activities, prepare accordingly."


